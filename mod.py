import sys
import pyautogui as pg

def dota():

    pg.FAILSAFE = False

    pg.PAUSE = 2

    pg.hotkey("win", "m")

    pg.PAUSE = 1

    pg.press("win")

    pg.PAUSE = 1

    pg.typewrite("Dota", interval=0.1)

    pg.PAUSE = 1

    pg.hotkey("Enter")
    sys.exit()

